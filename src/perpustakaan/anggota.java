    private void tataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tataMouseClicked
        // TODO add your handling code here:
        int i = tata.getSelectedRow();
        TableModel model = tata.getModel();
        
        if(i == -1){
            return;
        }
        
        String code = (String)tata.getValueAt(i,0);
        String code1 = (String)tata.getValueAt(i,1);
        String code2 = (String)tata.getValueAt(i,2);
        String code3 = (String)tata.getValueAt(i,3);
        String code4 = (String)tata.getValueAt(i,4);
        String code5 = (String)tata.getValueAt(i,5);
        
        nisn.setText(code);
        nama.setText(code1);
        
        try{
            int srow = tata.getSelectedRow();
            java.util.Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String)model.getValueAt(i,2));
            ttl.setDate(date);
        } catch (ParseException ex) { 
            Logger.getLogger(transaksi.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        jk.setText(code3);
        alamat.setText(code4);
        kelas.setText(code5);
    }//GEN-LAST:event_tataMouseClicked

