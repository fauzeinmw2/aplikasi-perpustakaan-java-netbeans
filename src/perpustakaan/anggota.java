    private void loaddata(){
       DefaultTableModel t = new DefaultTableModel();
        t.addColumn("NISN");
        t.addColumn("Nama");
        t.addColumn("TTL");
        t.addColumn("Jenis Kelamin");
        t.addColumn("Alamat");
        t.addColumn("Kelas");
        tata.setModel(t);
        try{
            res=stat.executeQuery("Select * from anggota");
            
            while (res.next()){
                t.addRow(new Object[]
                {
                    res.getString("nim"),
                    res.getString("nama"),
                    res.getString("ttl"),
                    res.getString("jenis_kelamin"),
                    res.getString("alamat"),
                    res.getString("kelas"),
                });
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

