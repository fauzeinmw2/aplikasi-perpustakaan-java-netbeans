    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String x = JOptionPane.showInputDialog(null, "Masukkan NISN!!");
        try{
            String cari = "select * from anggota where NIM='"+x+"'";
            
            Statement stat = con.createStatement();
            res = stat.executeQuery(cari);
            
            if(res.next()){
                System.out.println(res.getString(1));
                JOptionPane.showMessageDialog(null, "Data ditemukan");
                nisn.setText(res.getString(1));
                nama.setText(res.getString(2));
                
                try{
                    java.util.Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String)res.getString(3));
                    ttl.setDate(date);
                } catch (ParseException ex) { 
                    Logger.getLogger(transaksi.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                jk.setText(res.getString(4));
                alamat.setText(res.getString(5));
                kelas.setText(res.getString(6));
            } else {
                JOptionPane.showMessageDialog(null, "Data Tidak ditemukan");
            }
        } catch (Exception e){
            System.out.print(e);
            JOptionPane.showMessageDialog(null, "Koneksi Gagal");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        int coba = JOptionPane.showConfirmDialog(null, "Yakin untuk mengubah data ini?","Confirmation", JOptionPane.YES_NO_OPTION);
        try{
            String sql = "update anggota set NIM=?, Nama=?, TTL=?,Jenis_Kelamin=?,Alamat=?,Kelas=? where " + "NIM='"+nisn.getText()+"'";
            st=con.prepareStatement(sql);
            
            if(coba==0){
                try{
                    st.setString(1, nisn.getText());
                    st.setString(2, nama.getText());
                    st.setString(3, tLahir);
                    st.setString(4, jk.getText());
                    st.setString(5, alamat.getText());
                    st.setString(6, kelas.getText());
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Update data sukses");
                    loaddata();
                } catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Update data Gagal" + e);
                }
            }
        } catch(Exception e){
            
        }
    }//GEN-LAST:event_editActionPerformed

